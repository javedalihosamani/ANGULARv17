// Simple If Condition Statement
var max = 100;
var counter = 0;
if (counter < max) {
    counter++; // 0+1 = 1
}
console.log(counter);
// Simple If else Condition Statement
var max1 = 100;
var counter1 = 100;
if (counter1 < max1) {
    counter1++;
}
else {
    counter1 = 1;
}
console.log(counter1);
