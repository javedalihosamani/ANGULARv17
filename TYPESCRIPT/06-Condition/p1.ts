// Simple If Condition Statement
const max = 100;
let counter = 0;

if (counter < max) {
    counter++; // 0+1 = 1
}

console.log(counter);

// Simple If else Condition Statement
const max1 = 100;
let counter1 = 100;

if (counter1 < max1) {
    counter1++;
} else {
    counter1 = 1;
}

console.log(counter1);